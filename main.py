class Calculator:

    def add(self, a, b):
        self._a = a
        self._b = b
        return self._a + self._b

    def subtract(self, a, b):
        self._a = a
        self._b = b
        return self._a - self._b

    def multiply(self, a, b):
        self._a = a
        self._b = b
        return self._a * self._b

    def divide(self, a, b):
        self._a = a
        self._b = b
        return self._a / self._b


